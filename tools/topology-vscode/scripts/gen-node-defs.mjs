#!/usr/bin/env node
// GENERATOR: reads all nodes/*/SPEC.md files, emits src/webview/rf/nodes/node-defs.ts.
// Run: node scripts/gen-node-defs.mjs (from tools/topology-vscode/)

import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join, resolve } from "node:path";
import { parseSpec } from "./parse-spec.mjs";

const REPO_ROOT = resolve(import.meta.dirname, "../../../");
const NODES_DIR = join(REPO_ROOT, "nodes");
const OUT_FILE = resolve(
  import.meta.dirname,
  "../src/webview/rf/nodes/node-defs.ts"
);

function portExpr(port) {
  if (port.accent) return `{ id: "${port.id}", accent: "${port.accent}" }`;
  return `{ id: "${port.id}" }`;
}

function buildDef(view, ports) {
  const targets = ports.filter((p) => p.direction === "in");
  const sources = ports.filter((p) => p.direction === "out");

  const fields = [];
  fields.push(`defaultLabel: "${view.defaultLabel || view.kind}"`);
  fields.push(`bg: "${view.bg}"`);
  fields.push(`border: "${view.border}"`);
  fields.push(`text: "${view.text}"`);
  fields.push(`accent: "${view.accent}"`);
  if (view.minWidth) fields.push(`minWidth: ${view.minWidth}`);
  if (view.sublabel) fields.push(`sublabel: "${view.sublabel}"`);
  if (targets.length)
    fields.push(`targets: [${targets.map(portExpr).join(", ")}]`);
  if (sources.length)
    fields.push(`sources: [${sources.map(portExpr).join(", ")}]`);
  if (view.displays) {
    const items = view.displays
      .split(",")
      .map((d) => `"${d.trim()}"`)
      .join(", ");
    fields.push(`displays: [${items}]`);
  }
  return `{ ${fields.join(", ")} }`;
}

function main() {
  const dirs = readdirSync(NODES_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name);

  const entries = [];
  for (const dir of dirs) {
    const specPath = join(NODES_DIR, dir, "SPEC.md");
    let src;
    try {
      src = readFileSync(specPath, "utf8");
    } catch {
      continue;
    }
    const parsed = parseSpec(src);
    if (!parsed) continue;
    entries.push({ kind: parsed.view.kind, view: parsed.view, ports: parsed.ports });
  }

  const PRIORITY = ["Input", "ReadGate", "ChainInhibitor", "InhibitRightGate"];
  entries.sort((a, b) => {
    const ai = PRIORITY.indexOf(a.kind);
    const bi = PRIORITY.indexOf(b.kind);
    if (ai !== -1 && bi !== -1) return ai - bi;
    if (ai !== -1) return -1;
    if (bi !== -1) return 1;
    return a.kind.localeCompare(b.kind);
  });

  const lines = [
    `// GENERATED by scripts/gen-node-defs.mjs — do not edit. Source: nodes/<Kind>/SPEC.md`,
    ``,
    `export type DisplayKind = "queue" | "repeat" | "held";`,
    ``,
    `export interface NodePort {`,
    `  id: string;`,
    `  accent?: string;`,
    `}`,
    ``,
    `export interface NodeDef {`,
    `  defaultLabel: string;`,
    `  bg: string;`,
    `  border: string;`,
    `  text: string;`,
    `  accent: string;`,
    `  minWidth?: number;`,
    `  sublabel?: string;`,
    `  targets?: NodePort[];`,
    `  sources?: NodePort[];`,
    `  displays?: DisplayKind[];`,
    `}`,
    ``,
    `export const NODE_DEFS: Record<string, NodeDef> = {`,
    ...entries.map(
      (e) => `  ${e.kind}: ${buildDef(e.view, e.ports)},`
    ),
    `};`,
    ``,
  ];

  writeFileSync(OUT_FILE, lines.join("\n"), "utf8");
  console.log(`Wrote ${OUT_FILE} (${entries.length} entries)`);
}

main();
